select FIRST_NAME, LAST_NAME, d.DEPARTMENT_ID, DEPARTMENT_NAME from DEPARTMENTS d inner join EMPLOYEES e on d.DEPARTMENT_ID = e.DEPARTMENT_ID where
d.DEPARTMENT_ID in (80 , 40);

-- 6. FIND OUT CITY , COUNTRY_NAME OF THAT CITY , REGION_NAME OF THAT CITY
select CITY , COUNTRY_NAME,REGION_NAME from LOCATIONS l inner join COUNTRIES c on c.COUNTRY_ID = l.COUNTRY_ID
inner join REGIONS r on c.REGION_ID = r.REGION_ID;

-- 7. FIND OUT EMPLOYEES FIRST_NAME , JOB_TITLE , DEPARTMENT_NAME , CITY
select FIRST_NAME, JOB_TITLE, DEPARTMENT_NAME, city  from EMPLOYEES e inner join JOBS J on J.JOB_ID = e.JOB_ID
inner join DEPARTMENTS d on e.DEPARTMENT_ID = d.DEPARTMENT_ID
inner join LOCATIONS l on d.LOCATION_ID = l.LOCATION_ID;

-- 8. FIND OUT EMPLOYEES FIRST_NAME , JOB_TITLE , DEPARTMENT_NAME , CITY , COUNTRY_NAME , REGION_NAME
select FIRST_NAME, JOB_TITLE, DEPARTMENT_NAME, CITY, COUNTRY_NAME, REGION_NAME from EMPLOYEES
inner join JOBS J on J.JOB_ID = EMPLOYEES.JOB_ID
inner join DEPARTMENTS d on EMPLOYEES.DEPARTMENT_ID = d.DEPARTMENT_ID
inner join LOCATIONS l on d.LOCATION_ID = l.LOCATION_ID
inner join COUNTRIES c on l.COUNTRY_ID = c.COUNTRY_ID
inner join REGIONS r on c.REGION_ID = r.REGION_ID;;

